//创建一个自执行函数
//自执行函数在页面加载的时候就会执行,与全局定义的区别在于:不会创造全局变量,污染环境.
//与普通的方法的区别在于写法简单.
(function(){
    var name = "123";
    console.log(name);
})();
console.log("====================================================================");


//传递参数给自执行函数
(function(food){
    console.log("i am going to eat " + food);
})("fish");